package Variable;

public class ProgramThree {
    // I will learn about variable in java programming language
    public static void main(String[] args) {
    // to store a whole number we use int data type 
    int myAge = 25;
    System.out.println("My age is: " + myAge);
    // to store with the numbers after the decimal point we use double data type
    double price = 13.90;
    System.out.println("The price of the item is: " + price);
    // to store a single charecter we use char data types
    char grade = 'A';
    System.out.println("My final grade was : " + grade);
    // to store text we use String data type
    String name = "Patrick";
    System.out.println("My name is : " + name);
    // to store a true or false value we use boolean data type
    boolean isJavaFun = true;  
    System.out.println("Is Java fun? : " + isJavaFun);
    

}
}
